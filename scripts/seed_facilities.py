def slugify(name: str, city: str) -> str:
    """Generate URL slug: facility-name-city-nj"""
    text = f"{name} {city} nj".lower()
    text = re.sub(r"[^a-z0-9\s-]", "", text)
    text = re.sub(r"[\s-]+", "-", text).strip("-")
    return text

