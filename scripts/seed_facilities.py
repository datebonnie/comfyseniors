def main():
    # Add slugs and state
    rows = []
    for f in FACILITIES:
        row = {**f, "state": "NJ"}
        row["slug"] = slugify(f["name"], f["city"])
        if "is_featured" not in row:
            row["is_featured"] = False
        if "featured_since" not in row:
            row["featured_since"] = None
        rows.append(row)

    print(f"Seeding {len(rows)} facilities into Supabase...")

    # Insert in batches of 10
    for i in range(0, len(rows), 10):
        batch = rows[i:i+10]
        try:
            result = insert("facilities", batch, upsert=True)
            print(f"  Inserted batch {i//10 + 1}: {len(result)} rows")
        except Exception as e:
            print(f"  ERROR on batch {i//10 + 1}: {e}")

    print("Done!")


if __name__ == "__main__":
    main()
